const Alexa = require('ask-sdk-core');
const config = require('./config.json');
const bringApi = require('bring-shopping');

var bring = undefined;

async function getBring(){
    if (bring === undefined) {
        bring = new bringApi({mail: config.bringProperties.email, password: config.bringProperties.password});
        await bring.login();
    }
    
    return bring;
}

const LaunchRequestHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'LaunchRequest';
    },
    async handle(handlerInput) {

        const speakOutput = config.locales.launch;

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .getResponse();
    }
};

const GetItemsIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'GetItemsIntent';
    },
    async handle(handlerInput) {
        
        var speakOutput = config.locales.error;
        
        try {
            bring = await getBring();
            const translations = await bring.loadTranslations(config.bringProperties.locale);
            const items = await bring.getItems(config.bringProperties.listUUID);
            
            if(items.purchase.length <= 0){
                speakOutput = config.locales.empty;
            }else{
                speakOutput = config.locales.list;
                items.purchase.forEach(item => {
                    let localeItem = translations[item.name] ? translations[item.name] : item.name;
                    speakOutput += localeItem + ", "
                });
            }
            
        } catch (err) {
            speakOutput = config.locales.error;
        }

        return handlerInput.responseBuilder
            .speak(speakOutput)
            //.reprompt('add a reprompt if you want to keep the session open for the user to respond')
            .getResponse();
    }
};

const AddItemIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'AddItemIntent';
    },
    async handle(handlerInput) {
        
        var speakOutput = config.locales.error;
        
        try {
            const item = Alexa.getSlotValue(handlerInput.requestEnvelope, 'producto');
            bring = await getBring();
            await bring.saveItem(config.bringProperties.listUUID, item, "");
            speakOutput = `${config.locales.add} ${item}`;
        } catch (err) {
            speakOutput = config.locales.error;
        }

        return handlerInput.responseBuilder
            .speak(speakOutput)
            //.reprompt('add a reprompt if you want to keep the session open for the user to respond')
            .getResponse();
    }
};

